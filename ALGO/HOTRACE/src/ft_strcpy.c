#include "../includes/tools.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	char	*tmp;

	tmp = s1;
	while (*s2)
	{
		*s1 = *s2;
		s1++;
		s2++;
	}
	while (*s1)
	{
		*s1 = '\0';
		s1++;
	}
	return (tmp);
}

