#include <tools.h>

int	ft_strcmp(const char *s1, const char *s2)
{
	int	diff;

	diff = 0;
	while (*s1 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	diff = (*s1 > *s2) ? 1 : 0;
	diff = (*s1 < *s2) ? -1 : diff;
	return (diff);
}

