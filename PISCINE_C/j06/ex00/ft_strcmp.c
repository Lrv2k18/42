int	ft_strcmp(char *s1, char *s2)
{
	int	b;
	int	i;
	int	ok;
	
	ok = 0;
	b = 0;
	while (*s1 != '\0' && *s2 != '\0' && b == 0)
	{
		b = *s1 - *s2;
		if (b == 0)
		{
			s1++;
			s2++;
		}
	}
	b = *s1 - *s2;
	return (b);
}
